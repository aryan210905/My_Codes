a = "Aryan"
b = "Shah"
print("Hello, "+a)

# string concatination is done via '+' operator between the strings
print(a+b)

# we can store large multi-line strings in triple single quotes i.e. ''' string '''
c = '''Hello
Hi
I am Aryan
Hi Aryan. I am Jay
Hey Jay. Nice to meet you!
'''
print(c)

# we can print every character of the string by name[index] 
print(a[0])
print(a[1])
print(a[2])
print(a[3])
print(a[4])